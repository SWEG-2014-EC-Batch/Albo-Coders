#include <iostream>
using namespace std;

void mult(float num1, float num2) {
    float result = num1 * num2;
    cout << result << endl;
}

int main() {
    float num1, num2;
    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin >> num2;
    mult(num1, num2);
    return 0;
}
